package util;

public class AplicacaoExternaException extends Exception {

	private static final long serialVersionUID = 8524466012857346572L;

	public AplicacaoExternaException() {
		super();
	}

	public AplicacaoExternaException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AplicacaoExternaException(String arg0) {
		super(arg0);
	}

	public AplicacaoExternaException(Throwable arg0) {
		super(arg0);
	}

}
